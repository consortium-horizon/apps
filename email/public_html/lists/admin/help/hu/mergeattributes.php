<p>Megjegyzések a tulajdonságok összevonásáról.</p>
<p>A tulajdonságok összevonása azt jelenti, hogy a felhasználók értékei ugyanazok maradnak, viszont
a tényleges tulajdonság, melyhez tartoznak, összevonásra kerül. A megmaradó tulajdonság
az első (lista sorrend szerint, ahogy az oldalon látható).</p>
<ul>
<li>Csak az azonos típusú tulajdonságokat vonhatja össze</li>
<li>Az összevonás során az első tulajdonság értékét tartja meg, ha létezik, egyéb esetben
felülírja az összevonás alatt lévő tulajdonság értéke. Ez adatvesztést okozhat,
amennyiben mindkét tulajdonságnak volt értéke. </li>
<li><i>Jelölőnégyzet</i> típusú tulajdonságok összevonásakor az eredményül kapott, összevont tulajdonság <i>jelölőnégyzet-csoport</i> típusú lesz.</li>
<li>Egy másikkal összevonás alatt lévő tulajdonságok az összevonás után törlésre kerülnek.</li>
</ul>
