<h3>Остановить отправку после</h3>
<p>Задайте значение для того, чтобы остановить отправку кампании после определённого времени.</p>
<p>Это очень полезно в тех случаях, когда Ваша кампания затрагивает события в будущем. Используя эту настройку, Вы можете избежать отправку сообщений подписчикам после того, как Ваше событие завершится.</p>
