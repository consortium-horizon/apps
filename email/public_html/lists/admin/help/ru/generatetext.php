<h3>Создать текстовую версию письма на основе HTML</h3>

<p>Данная возможность позволит преобразовать содержимое Вашей кампании в формате HTML в текстовую версию письма.</p>
<p>Всё содержимое, которое уже напечатано в области ввода текста, будет удалено.</p>

<p><b>Внимание:</b> процесс не будет успешно завершён при большом объёме HTML-содержимого.</p>
