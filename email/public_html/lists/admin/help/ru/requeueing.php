<h3>Повторять рассылку</h3>

<p>Когда Вы выбираете &laquo;повторять рассылку&raquo;, рассылка вновь попадает в очередь, после того как отправка завершится. 
Запрет на отправку кампании будет установлен в соответствии с указанным временем и частотой повторения, которая может быть указана здесь же. </p>

<p>Это позволит отправлять письма тем подписчикам, которые подписались на рассылку уже после того, как Вы отправили кампанию.</p>

