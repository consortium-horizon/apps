<h3>Код Google Tracking</h3>

<p>Использовании Google Analytics на Вашем сайте - это отличный способ для отслеживания результатов Ваших кампаний.</p>
<p>При выборе этой опции, ко всем ссылкам будет добавлен код отслеживания. После отправки кампании, в Google Analytics можно будет посмотреть результаты, для этого перейдите в раздел Источники трафика -&gt; Кампании (на англ.: Acquisition -&gt; Campaigns).</p>



