
<h3>Versione di testo</h3>

<p>Tutte le email, anche quando il contenuto &egrave; in HTML, devono avere una versione di testo. Questo &egrave; imposto dagli standard di internet e phplist non pu&ograve; cambiarlo. Se lasciate questo campo vuoto, il sistema phplist generer&agrave; automatimante il contenuto formato testo dal vostro HTML. In alternativa, potete inserire qui il contenuto del messaggio.</p>

<h3>A chi arriva la versione testo?</h3>

<p>La versione testo verr&agrave; mostrata a due tipi di utente.</p>

<ol>
<li>Gli utenti che hanno impostato tra le loro preferenze di phplist quella di ricevere email di testo.br/>
<i>A questi utenti verranno inviate solo email di testo.</i></li>
<li>Agli utenti che hanno client di posta elettronica che mostrano solo versioni in HTML.<br/>
Alcuni utenti usano programmi che non riescono a supportare l'HTML. In quel caso, vedranno solo la versione di testo. Questo viene stabilito alla fine del canale di comunicazione dell'utente.
<i>Questi utenti riceveranno entrambi i tipi di e-mail, quelle contenenti sia la versione HTML che la versione testo, ma il loro sistema decider&agrave; di mostrare quella di testo.</i>
</li>
</ol>
  