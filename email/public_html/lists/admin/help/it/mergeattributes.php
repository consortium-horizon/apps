<p>Note per unire gli attributi.</p>
<p>Unire gli attributi significa che i valori per gli utenti rimarranno gli stessi, ma l'attributo reale sar&agrave unito. L'attributo che viene mantenuto &egrave; il primo (tramite l'ordinamento di lista, come potete vedere nella pagina).</p>
<ul>
<li>Potete unire solo gli attributi dello stesso tipo</li>
<li>Quando unite, il valore del primo attributo viene mantenuto se esiste, altrimenti viene sovrascritto dal valore dell'attributo a cui viene unito. Questo pu&ograve; causare perdita di dati, nel caso in cui entrambi gli attributi abbiano un valore.</li>
<li>Se unite attributi del tipo <i>checkbox</i> l'attributo risultante dall'unione diventa di tipo <i>checkboxgroup</i>.</li>
<li>Gli attributi che sono stati uniti correttamente in un'altro vengono cancellati dopo l'unione.</li>
</ul>
