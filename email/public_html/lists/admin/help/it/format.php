<h3>Formato del messaggio</h3>
Se usate "automatico" il messaggio verr&acute; classificato come HTML non appena un tag HTML (&lt; ... &gt;) viene trovato nel testo.
</p><p><b>E' pi&ugrave; sicuro se lasciate la funzione "Automatico"</b></p><p>
Se non siete sicuri che "Automatico" funzioni e il messaggio che incollate &egrave; stato formattato in HTML scegliete "HTML".
I riferimenti ad oggetti esterni (es. immagini) devono avere l'URL completo, ovvero che inizi con http:// (a differenza delle immagini usate per i modelli).
Per tutto il resto siete voi i responsabili della formattazione del testo.
<p>Se volete forzare un messaggio in formato testo, selezionate "Testo".
</p><p>
Questa informazione &egrave; usata per creare una versione testuale del testo formattato in HTML o viceversa (testo HTML di un messaggio testuale). 
La formattazione funziona in questo modo.</br>
Testo originale in HTML -&gt; testo<br/>
<ul>
<li><b>Grassetto</b> il testo sar&aacute; all'interno di due <b>caratteri *</b>, <b>corsivo</b> tra due <b>caratteri /</b></li>
<li>I collegamenti presenti nel testo saranno rimpiazzati con il testo, seguito dall'URL tra due parentesi/li>
<li>Grandi blocchi di testo saranno mandati a capo ogni 70 caratteri</li>
</ul>
Testo originale in formato testuale -&gt; HTML<br/>
<ul>
<li>Due a capo saranno rimpiazzati da un &lt;p&gt; (paragrafo)</li>
<li>Un singolo a capo sar&aacute; rimpiazzato da un &lt;br /&gt; (a capo/interruzione di linea)</li>
<li>Potete cliccare sugli Indirizzi email</li>
<li>Potete cliccare sugli URL. Gli URL vengono riconosciuti se sono in una di questi formati:<br/>
<ul><li>http://some.website.url/some/path/somefile.html
<li>www.websiteurl.com
</ul>
I link creati avranno il campo classe (per modificarne l'aspetto tramite fogli di stile) "url" e target "_blank".
</ul>
<b>Attenzione</b>: indicando che il vostro messaggio &egrave; testuale, ma incollando il testo in HTML, questo verr&aacute; inviato come tale (con tutti i tag visibili e non interpretati) anche agli utenti che vogliono ricevere i messaggi in formato testo.
