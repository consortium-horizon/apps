<p>Beim Zusammenf&uuml;hren von Attributen bleiben die Attributwerte erhalten.
Es werden lediglich mehrere Attribute zu einem einzigen Attribut kombiniert.
Das verbleibende Attribut ist dasjenige, das auf dieser Seite zuoberst aufgelistet ist.</p>

<p>Bitte beachten Sie:</p>

<ul>
	<li>Es k&ouml;nnen nur Attribute desselben Typs zusammengef&uuml;hrt werden.<br />
		&nbsp;&nbsp;</li>
	<li>Falls das erste Attribut einen Wert besitzt, bleibt dieser erhalten.
		Falls nicht, so wird der Wert desjenigen Attributs &uuml;bernommen, das mit dem ersten Attribut zusammengef&uuml;hrt werden soll.
		<b>Falls beide Attribute einen Wert besitzen, gehen Daten verloren</b> (weil dann nur der Wert des ersten Attributs erhalten bleibt).<br />
		&nbsp;&nbsp;</li>
	<li>Das Zusammenf&uuml;hren von Attributen des Typs "Checkbox" ergibt ein Attribut vom Typ "Checkbox-Gruppe".<br />
		&nbsp;&nbsp;</li>
	<li>Attribute, welche in ein anderes Attribut &uuml;berf&uuml;hrt wurden, werden anschliessend gel&ouml;scht.<br />
		&nbsp;&nbsp;</li>
</ul>
