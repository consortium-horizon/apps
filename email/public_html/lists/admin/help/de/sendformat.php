Falls eine Nachricht HTML-formatiert ist k&ouml;nnen Sie hier festlegen, wie sie verschickt werden soll:<br />
<ul>
<li><b>HTML</b> - Abonnenten, welche das HTML-Format vorziehen, erhalten ein HTML-Mail; alle andere erhalten ein reines Text-Mail.</li>
<li><b>Text</b> - Alle Abonnenten erhalten die Nachricht im Text-Format (unabh&auml;ngig von ihrer Pr&auml;ferenz).</li>
<li><b>Text plus HTML (Multipart)</b> - Die Nachricht wird im Multipart-Format verschickt und enth&auml;lt sowohl eine reine Text- als auch eine HTML-Version.
	Diese Methode erzeugt zwar gr&ouml;ssere Mails, erh&ouml;ht aber die Wahrscheinlichkeit, dass die Nachricht bei jedem Empf&auml;nger optimal dargestellt wird.</li>
<li><b>PDF</b> - Die Text-Version der Nachricht wird als PDF-Datei verschickt.</li>
<li><b>Text plus PDF</b> - Die Nachricht wird als reine Text-Mail mit einem PDF-Anhang verschickt.</li>
</ul>

<b>Bitte beachten Sie:</b> Die PDF-Version einer Nachricht enth&auml;lt immer die Text-Version, nicht die HTML-Version.