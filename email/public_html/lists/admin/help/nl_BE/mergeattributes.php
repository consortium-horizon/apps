<p>Tips voor het samenvoegen van attributen.</p>
<p>Samenvoegen van attributen betekent dat de waarden van de gebruikers hetzelfde zullen blijven, maar dat
het echte attribuut waartoe ze behoren is samengevoegs in een. Het attribuut dat zal overblijven
is het eerste (volgens volgorde, zoals je het ziet op de pagina).</p>
<ul>
<li>U kan attributen enkel samenvoegen als ze van hetzelfde type zijn</li>
<li>Bij het samenvoegen zal de waarde van het eerste attribuut worden behouden als het bestaat, anders 
zal het worden overschreven door de waarde van het attribuut waarin het wordt samengevoegd. Dit kan verlies veroorzaken van data, 
in het geval dat beide attributen een waarde hadden. </li>
<li>Als u attributen van het type <i>checkbox</i> samenvoegd, dan zal het uiteidelijke samengevoegd attribuut van het type <i>checkboxgroup</i> zijn.</li>
<li>Attributen die worden samengevoegd met een ander worden verwijdert na de samenvoeging.</li>
</ul>
