<b>تحریم یک پیام</b>
<p>می‌توانید با تحریم یک پیام از فرستاده شدن پیام تا تاریخ و زمانی که خودتان مشخص میکنید جلوگیری کنید.
در حالت پیشفرض این تاریخ و زمان، امروز و ساعت 0:00 خواهد بود، که فوراً فرستاده خواهد شد.</p>
<p><b>لطفاً توجه کنید</b>: تحریم بر روی زمانی که فرستادن پیامتان آغاز میشود تاثیر میگذارد و به این معنی نیست که کاربران پیامها را در آن زمان در صندوقهایشان دریافت خواهند کرد.
</p>