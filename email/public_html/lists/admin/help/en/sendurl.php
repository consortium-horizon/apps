
<h3>Send a web page</h3>

<p>To send the contents of a webpage to your subscribers, simply enter the URL in the box below</p>

<p>You can specify some subscriber specific data in your URL:

<ul>
<li>[email] - the email of the subscriber</li>
<li>[id] - the subscriber ID</li>
<li>[uniqid] - the Unique Code for the subscriber</li>
<li>[htmlemail] - the HTML preference for the subscriber</li>
</ul>

</p>

<p><strong>Warning, using user specific URLs will dramatically slow down the sending of messages</strong></p>
