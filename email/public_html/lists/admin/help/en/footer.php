
<h3>The Message Footer</h3>

<p>The message footer serves several purposes and is very important. When the message is forwarded to some other email address, the footer will be changed to be the one for "forwarded messages", which will allow
specifying different content for individuals who are not subscribed to your newsletters, to invite them to subscribe.</p>

<p>You can set the default footer in the
<?php

## for translators, you can translater the word "Configuration" below
echo PageLink2('configure&id=messagefooter','Configuration');
?>
 </p>

<p>You can use several placeholders, which will help your subscribers identify the email they receive and what to do with it.

<ul>
<li><b>[UNSUBSCRIBEURL]</b> - the personalised location for the subscriber to unsubscribe</li>
<li><b>[PREFERENCESURL]</b> - the personalised location for the subscriber to change their details</li>
<li><b>[FORWARDURL]</b> - the personalised location for the subscriber to forward the message to someone else</li>
<li><b>[EMAIL]</b> - the email address that received this message</li>
<li><b>[USERID]</b> - a unique string for this subscriber</li>
<li><b>[USERTRACK]</b> - the tracking code for "opens"</li>
</ul>
</p>

