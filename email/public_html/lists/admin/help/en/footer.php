<h3>Formatting</h3>
<p>You can use HTML in the footer. Try to keep it simple. The footer for the text version of emails will be generated from the HTML, by stripping HTML tags. If you do not use any HTML in the footer, the HTML version will be generated by adding &lt;br/&gt; for line endings.</p>


</p>
