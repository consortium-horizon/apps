<p>Uwagi do łączenia atrybutów.</p>
<p>Łączenie atrybutów oznacza, że wartości dla użytkowników pozostaną te same ale
aktualne atrybuty do którego należą zostanie połączony w jeden. Atrybut, który pozostanie
to ten pierwszy (z listy na stronie).</p>
<ul>
<li>Mozna łączyć tylko atrybuty tego samego typu</li>
<li>Podczas łączenia wartość pierwszego atrybutu zostanie zachowana jeśli istnieje, w przeciwnym razie
zostanie nadpisana przez wartość atrybutu w który jest łączone. To może spowodować utratę
danych, w przypadku gdy obydwa atrybuty mają wartość. </li>
<li>Jeśli łączysz atrybuty tyou <i>pole wyboru</i> wynikiem łączenia będzie atrybut typu <i>grupa pól wyboru</i>.</li>
<li>Atrybuty łączone w inne zostaną usunięte po połączeniu.</li>
</ul>
