
<h3>Text opmaak</h3>

<p>Iedere e-mail bericht, zelf al bevat de inhoud HTML onderdelen, moet in tekst opmaak kunnen worden verzonden. Dit is bepaald in de internet standaarden. Als je dit veld leeg laat, dan zal phpList automatisch tekst genereren uit HTML opmaak. Je kunt ook als aternatief voor HTML opmaak je eigen tekst in het veld invoegen.</p>

<h3>Wie krijgt de e-mail berichte met tekst opmaak? </h3>

<p>De versie met tekst opmaak zal worden verzonden aan twee soorten abonnees:</p>

<ol>
<li>Abonnees die bij de instellingen hebben opgegeven een versie met tekst opmaak te willen ontvangen.<br/>
<i>Aan deze abonnees zullen alleen e-mail berichtens met een tekst opmaak worden verzonden.</i></li>
<li>Abonnees die e-mail programma's hebben welke een HTML opmaak niet kunnen weergeven.
Sommige abonnees hebben e-mail programma's welke geen HTML opmaak kunnen verwerken. In dat geval zal de tekst opmaak worden getoond. Dit besluit valt aan de abonneezijde van de communicatie. <em>Deze abonnees zullen een e-mail bericht krijgen met zowel een tekst als HTML opmaak. Vervolgens besluit het e-mail programma alleen de versie met tekst opmaak te laten zien.</em></li>
</ol>

