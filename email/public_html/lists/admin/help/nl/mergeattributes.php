<p><strong>Tips voor het samenvoegen van velden (ook wel: attributen).</strong></p>
<p>Samenvoegen van velden betekent dat de waarden van de gebruikers hetzelfde zullen blijven, maar dat
het veld waartoe ze behoren is samengevoegd in een veld. Het veld dat zal overblijven
is het eerste (volgens volgorde, zoals je deze ziet op de pagina).</p>
<ul>
<li>Je kunt velden enkel samenvoegen als ze van hetzelfde type zijn</li>
<li>Bij het samenvoegen zal de waarde van het eerste veld worden behouden als het bestaat, anders 
zal het worden overschreven door de waarde van het veld waarin het wordt samengevoegd. Dit kan verlies veroorzaken van data, 
in het geval dat beide velden een waarde hadden. </li>
<li>Als je velden van het type <i>checkbox</i> samenvoegd, dan zal het uiteidelijke samengevoegd veld van het type <i>checkboxgroup</i> zijn.</li>
<li>Velden die worden samengevoegd met een ander worden verwijderd na de samenvoeging.</li>
</ul>
