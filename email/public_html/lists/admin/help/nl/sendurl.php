
<h3>Het versturen van een pagina van een website</h3>

<p>Om de inhoud van de pagina van een website naar de abonnees te sturen moet de URL in het veld hieronder worden ingevuld.</p>
<p>Er kan abonnee specifieke data in de URL worden verwerkt:</p>
<ul>
  <li>[email] - het emailadres van de abonnee</li>
  <li>[id] - het abonnee ID</li>
  <li>[uniqid] - de unieke code van de abonnee</li>
  <li>[htmlemail] - de HTML instelling van de abonnee</li>
</ul>
<p><strong>Let op:</strong> door gebruik te maken van abonnee specifieke URL's zal het verzenden van de berichten veel langer duren.</p>
