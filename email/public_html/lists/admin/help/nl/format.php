<h3>Opmaak  van het bericht</h3>
Als je "auto detectie" gebruikt, dan zal het bericht worden opgemaakt met HTML zodra er een HTML tag (&lt; ... &gt;) in voorkomt.
</p>
<p><b>Is het  veilig om "Auto detectie" aangevinkt te laten</b>?</p>
<p>
Als je er niet zeker bent dat "auto detectie" goed functioneert, en het bericht dat je maakt moet in HTML opgemaakt worden, kies dan voor "HTML".
Externe links naar bronnen (bijvoorbeeld afbeeldingen) hebben de volledige URL nodig, startend met http://. 

<p>Als je het bericht als in tekst opmaak wilt verzenden, vink dan "Tekst" aan.
</p>
<p>
Deze keuze wordt gebruikt om de tekstopmaak van een HTML bericht of een HTML opmaak van een tekstbericht te maken.
De opmaak zal er als volgt uitzien:<br/>
<br />
<br />
Origineel is HTML -&gt tekst
<ul>
<li>Vette tekst zal ingesloten zitten tussen *-tekens, cursieve tekst with <b>/</b>-tekens</li>
<li>Links rond tekst zullen worden vervangen door de tekst, gevolgd door de URL tussen haakjes</li>
<li>Grote blokken tekst zullen worden verdeeld bij kolom 70</li>
</ul>
Origineel is Tekst -&gt; HTML<br/>
<ul>
<li>Dubbele nieuwe regels zullen worden vervangen door een &lt;p&gt; (paragraaaf)</li>
<li>Enkele nieuwe regels lijnen zullen worden vervangen door een &lt;br /&gt; (nieuwe regel)</li>
<li>Email addressen zullen klikbaar worden gemaakt</li>
<li>URLs zullen klikbaar worden. URLs mogen in de volgende vorm staan:<br/>
<ul>
<li>http://een.website.nl/pad/naar/eenbestand.html
<li>www.websiteurl.nl
</ul>
De aangemaakte links zullen de standaard de opmaak (css) "url" hebben en als doel  "_blank".
</ul>
<b>Let op</b>
: als je aangeeft dat je bericht een tekst opmaak heeft en je plakt toch tekst met een HTML opmaak in de editor, dan zal de tekst en de HTML codes als platte tekst aan gebruikers worden gezonden welke hebben aangegeven e-mail berichten met tekst opmaak te willen ontvangen.
