<h3>Перестроение индексов базы данных.</h3>
<p>Скрипт на этой странице проверит индексы в базе данных и в случае их отсутствия пересоздаст их. При наличии большого количества записей в базе может истечь срок ожидания.</p>
<p>Если это произошло, обновите страницу несколько раз и попробуйте снова.</p>


