<p>Здесь необходимо установить некоторые параметры Вашего phpList.<br/>
При настройке можно использовать метки (Placeholders).
Они заключены внутри квадратных скобок и выглядят примерно так - <b>[НАЗВАНИЕ]</b>, где <i>НАЗВАНИЕ</i> внутри скобок может различаться.</p>
<p>Некоторые полезные метки:</p>

<div class="placeholders">
<ul>
<li>WEBSITE - указанный Вами адрес сайта</li>
<li>DOMAIN - указанное Вами доменное имя</li>
<li>SUBSCRIBEURL - ссылка для оформления подписки</li>
<li>UNSUBSCRIBEURL - ссылка для отказа от подписки</li>
<li>BLACKLISTURL - ссылка для отказа от подписки неопознанными подписчиками</li>
<li>PREFERENCESURL - ссылка на страницу, где подписчики могут изменить свои учётные данные</li>
<li>CONFIRMATIONURL - ссылка на страницу, где подписчики могут подтвердить своё согласие на рассылку</li>
</ul>
</div>
