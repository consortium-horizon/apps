<p>Den här sidan låter dig skapa sidor som används av användare för att anmäla sig till dina listor.</p>
<p>Du kan välja en uppsättning attribut och listor som har definierats för ditt system. Om en lista eller ett
attribut inte listas här behöver du lägga till dom först. Standardvärden för attribut är dina
globala attributvärden, men du kan återsortera dem här och lägga till olika standardvärden. Du kan ocksp
ändra huruvida ett attribut är obligatoriskt eller ej. Detta kommer endast påverkar obligatoriskheten för attributen
när användare använder den här sidan för att anmäla sig.</p>
