<p class="information">Här kan du specifiera unika systemvärden för din hemsida.<br/>
Du kan använda särskilda <b>platshållare</b> för dina värden.
En platshållare ser ut så här: <b>[NÅGONTEXT]</b>, där <i>NÅGONTEXT</i> kan varieras.
Några användbara platshållare är:
</p>
<div class="placeholders">
<ul>
<li>WEBSITE - den hemsidesadress som du har angett</li>
<li>DOMAIN - den domän som du har angett</li>
<li>SUBSCRIBEURL - URL-adressen till din anmälningssida</li>
<li>UNSUBSCRIBEURL - URL-adressen till avanmälningssidan</li>
<li>BLACKLISTURL - URL-adressen till avanmälningssidan för okända användare</li>
<li>PREFERENCESURL - URL-adressen till sidan där medlemmar kan uppdatera sina detaljer</li>
<li>CONFIRMATIONURL - URL-adressen till sidan där användare kan bekräfta sin anmälan</li>
</ul>
</div>
