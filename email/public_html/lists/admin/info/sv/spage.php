<h3>Skapa flera anmälningssidor</h3>
<p>Den här sidan låter dig skapa sidor som används av medlemmar för att anmäla sig till dina listor.</p>
<p>Du kan välja en uppsättning attribut och listor som är definierade i ditt system. Standardvärden för attributen är
de globala attributvärdena, men du kan inte sortera om dem eller lägga till olika standardvärden. Du kan också
ändra huruvida ett attribut är obligatoriskt eller ej. Detta kommer bara påverka huruvida attributen är obligatoriska
då en anmälningssida används av en ny medlem.</p>
