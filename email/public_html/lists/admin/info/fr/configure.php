qui a &eacute;t&eacute; d&eacute;finie par le concepteur du programme.
Ci-dessous, vous trouverez une liste de quelques codes-raccourcis utiles install&eacute;s par d&eacute;faut 
que vous pouvez utiliser.
</p>
