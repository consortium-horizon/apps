<p>設定樣版：可使用 HTML 及 <i>自訂標籤</i>，如 <b>[CONTENT]</b>. 信件內容會在此顯示 </P>
<p>除了 [CONTENT] 以外，還可以新增 [FOOTER] 及 [SIGNATURE] 來加入首尾或簽名檔的內容</p>
<p>若要加入圖片，需要使用完整的 URL 路徑，如 http"//www.example.com/image.jpg ，或是將圖片透過 HTML 編輯器上傳，由系統自動來處理圖片的連結</p>
<p><b>使用者開信及點閱追蹤</b></p>
<p>可在 HTML 的 Email 中，加入 [USERID] 的自訂標籤來追蹤使用者，但需要增加系統程式的判斷，當系統收到使用者 ID  [USERID] 時要做什麼處理，最簡單的方式為加入 [USERTRACK] ，系統會在信件中增加一個隱藏的圖片來追縱使用者是否有開信</p>
