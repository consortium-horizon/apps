<h1>Tworzenie wielu stron rejestracji.</h1>
<p>Ta strona pozwala na tworzenie stron, które są używane przez użytkowników w celu wpisania się na listę.</p>
<p>Możesz wybrać dowolny zestaw atrybutów i list zdefiniowanych w systemie. Wartości atrybutów są pobrane z 
globalnych wartości, ale możesz zmianić ich kolejność i dodać inne wartości domyślne. Możesz również
zmienić, czy atrybut jest wymagany czy nie. Będzie to miało wpływ tylko na wymagane atrybuty
gdy użytkownicy będą używali strony do zarejestrowania się.</p>
