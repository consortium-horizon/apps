<p>Tutaj możesz określić prawidłowe wartości dla Twojej witryny.<br/>
Możesz uzyć niektórych <b>Symboli</b> w woich ustawieniach.
Symbole wyglądają jak <b>[JAKIŚTEKST]</b>, gdzie <i>JAKIŚTEKST</i> może być różny.
Niektóre przydatne symbole:
<ul>
<li>WEBSITE - adres, który wpisałeś dla Twojej strony
<li>DOMAIN - tekst, który wpisałeś dla Twojej domeny
<li>SUBSCRIBEURL - loklaizacja strony rejestrowania
<li>UNSUBSCRIBEURL - loklaizacja strony wyrejestrowania
<li>BLACKLISTURL - loklaizacja strony wyrejestrowania dla nieznanych użytkowników
<li>PREFERENCESURL - lokalizacja strony gdzie użytkownicy mogą aktualizować swoje dane
<li>CONFIRMATIONURL - loklizacja strony gdzie użytkownicy muszą potwierdzić swoją rejestrację
</ul>
