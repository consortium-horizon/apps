<p>Ta strona umożliwia tworzenie stron, których użytkownicy będa używali w celu wpisania się na listy.</p>
<p>Możesz wybrać dowolny zestaw atrybutów i list zdefiniowanych w systemie. Jeśli lista albo
atrybut nie jest tutaj wyświetlony, musisz najpierw go dodać. Wartości atrybutów są pobrane z 
globalnych wartości, ale możesz zmianić ich kolejność i dodać inne wartości domyślne. Możesz również
zmienić, czy atrybut jest wymagany czy nie. Będzie to miało wpływ tylko na wymagane atrybuty
gdy użytkownicy będą używali strony do zarejestrowania się.</p>