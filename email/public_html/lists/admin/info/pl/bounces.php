<p>To jest lista odebranych wiadomości zwrotnych (zwrotów). W większości przypadków będą one identyfikowane dla użytkownika
oraz dla wiadomości. Jeśli utuchomiłeś live system, nie usuwaj zwrotów, ponieważ system nie będzie w stanie
zidentyfikować kolejnych zwrotów i powrócą one ponownie. Aktualnie możesz tylko oglądać wiadomości zwrotne,
nie ma możliwości (jeszcze) aby zmienić system identyfikacji wiadomości zwrotnych. W pewnych przypadkach
może to oznaczać, że widomość może zostać oznaczona jako zwrot, podczas gdy była to tylko wiadomość typu "Mailbox full".</p>
